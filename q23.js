var carUsed = true;
var carWheels = 2;
var carAirbags = 6;
var carPrice = 50;
// true statements
console.log("Is car === 'subaru'? I predict True.");
console.log(car === 'subaru');
console.log("Is carUsed === true ? I predict True.");
console.log(carUsed === true);
console.log("Is carWheels === 2 ? I predict True.");
console.log(carWheels === 2);
console.log("Is carAirbags !== 2 ? I predict True.");
console.log(carAirbags !== 2);
console.log("Is carPrice === 50 ? I predict True.");
console.log(carPrice === 50);
// false statement
console.log("Is car !== 'subaru'? I predict False.");
console.log(car !== 'subaru');
console.log("Is carUsed !== true ? I predict False.");
